#include <stdio.h>

float degreeToFarenheit(float degree)
{
    return degree * 1.8 + 32;
}

int main(void)
{
    for (float i = -20; i < 30; i++)
    {
        printf("Value of %.1f\370C corresponds to %.1f\370F\r\n", i, degreeToFarenheit(i));
    }
    return 0;
}